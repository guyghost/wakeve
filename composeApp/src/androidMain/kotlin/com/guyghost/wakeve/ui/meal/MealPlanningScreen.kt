                        dietaryRestrictions.size
                    ),
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.error
                )
            }
        }
    }
}

/**
 * Filter chips row
 */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun MealFiltersRow(
    selectedType: MealType?,
    selectedStatus: MealStatus?,
    onTypeSelected: (MealType) -> Unit,
    onStatusSelected: (MealStatus) -> Unit
) {
    Column(
        modifier = Modifier
            .fillMaxWidth()
            .padding(horizontal = 16.dp),
        verticalArrangement = Arrangement.spacedBy(8.dp)
    ) {
        // Type filters
        Text(
            stringResource(R.string.meal_type_label),
            style = MaterialTheme.typography.labelMedium,
            color = MaterialTheme.colorScheme.onSurfaceVariant
        )
        
        Row(
            modifier = Modifier.fillMaxWidth(),
            horizontalArrangement = Arrangement.spacedBy(8.dp)
        ) {
            MealType.entries.forEach { type ->
                val typeLabel = getMealTypeLabel(type)
                val selectedState = stringResource(if (selectedType == type) R.string.meal_selected_state else R.string.meal_unselected_state)
                val typeDescription = stringResource(
                    R.string.a11y_meal_filter_type,
                    typeLabel,
                    selectedState
                )
                FilterChip(
                    selected = selectedType == type,
                    onClick = { onTypeSelected(type) },
                    modifier = Modifier.clearAndSetSemantics {
                        contentDescription = typeDescription
                    },
                    label = { Text(typeLabel) }
                )
            }
        }
        
        // Status filters
        Text(
            stringResource(R.string.meal_status_label),
            style = MaterialTheme.typography.labelMedium,
            color = MaterialTheme.colorScheme.onSurfaceVariant
        )
        
        Row(
            modifier = Modifier.fillMaxWidth(),
            horizontalArrangement = Arrangement.spacedBy(8.dp)
        ) {
            MealStatus.entries.forEach { status ->
                val statusLabel = getMealStatusLabel(status)
                val selectedState = stringResource(if (selectedStatus == status) R.string.meal_selected_state else R.string.meal_unselected_state)
                val statusDescription = stringResource(
                    R.string.a11y_meal_filter_status,
                    statusLabel,
                    selectedState
                )
                FilterChip(
                    selected = selectedStatus == status,
                    onClick = { onStatusSelected(status) },
                    modifier = Modifier.clearAndSetSemantics {
                        contentDescription = statusDescription
                    },
                    label = { Text(statusLabel) }
                )
            }
        }
    }
    
    Spacer(Modifier.height(8.dp))
}

/**
 * Daily schedule section (meals grouped by date)
 */
@Composable
fun DailyScheduleSection(
    schedule: DailyMealSchedule,
    onMealClick: (Meal) -> Unit,
    onDeleteClick: (Meal) -> Unit
) {
    Column(
        verticalArrangement = Arrangement.spacedBy(8.dp)
    ) {
        // Date header
        val dateFormatter = DateTimeFormatter.ofLocalizedDate(FormatStyle.FULL)
            .withLocale(LocalConfiguration.current.locales[0])
        val formattedDate = try {
            LocalDate.parse(schedule.date).format(dateFormatter)
        } catch (e: Exception) {
            schedule.date
        }
        
        Text(
            formattedDate,
            style = MaterialTheme.typography.titleMedium,
            fontWeight = FontWeight.Bold,
            color = MaterialTheme.colorScheme.primary
        )
        
        // Meals for this date
        schedule.meals.sortedBy { it.time }.forEach { meal ->
            MealCard(
                meal = meal,
                onClick = { onMealClick(meal) },
                onDeleteClick = { onDeleteClick(meal) }
            )
        }
    }
}

/**
 * Individual meal card
 */
@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun MealCard(
    meal: Meal,
    onClick: () -> Unit,
    onDeleteClick: () -> Unit
) {
    val statusLabel = getMealStatusLabel(meal.status)
    val openDescription = stringResource(R.string.a11y_meal_open, meal.name, statusLabel)
    val deleteDescription = stringResource(R.string.a11y_meal_delete, meal.name)
    Card(
        modifier = Modifier
            .fillMaxWidth()
            .clickable(onClick = onClick)
            .clearAndSetSemantics {
                contentDescription = openDescription
            },
        colors = CardDefaults.cardColors(
            containerColor = when (meal.status) {
                MealStatus.COMPLETED -> MaterialTheme.colorScheme.tertiaryContainer
                MealStatus.IN_PROGRESS -> MaterialTheme.colorScheme.primaryContainer
                MealStatus.CANCELLED -> MaterialTheme.colorScheme.errorContainer
                else -> MaterialTheme.colorScheme.surfaceVariant
            }
        )
    ) {
        Row(
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp),
            horizontalArrangement = Arrangement.SpaceBetween,
            verticalAlignment = Alignment.CenterVertically
        ) {
            Column(
                modifier = Modifier.weight(1f),
                verticalArrangement = Arrangement.spacedBy(4.dp)
            ) {
                // Time + Type badge
                Row(
                    horizontalArrangement = Arrangement.spacedBy(8.dp),
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    Text(
                        meal.time,
                        style = MaterialTheme.typography.titleMedium,
                        fontWeight = FontWeight.Bold
                    )
                    
                    AssistChip(
                        onClick = {},
                        label = { Text(getMealTypeLabel(meal.type)) },
                        leadingIcon = {
                            Icon(
                                getMealTypeIcon(meal.type),
                                contentDescription = null,
                                modifier = Modifier.size(16.dp)
                            )
                        }
                    )
                }
                
                // Meal name
                Text(
                    meal.name,
                    style = MaterialTheme.typography.bodyLarge
                )
                
                // Location
                meal.location?.let { location ->
                    Row(
                        horizontalArrangement = Arrangement.spacedBy(4.dp),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        Icon(
                            Icons.Default.LocationOn,
                            contentDescription = null,
                            modifier = Modifier.size(16.dp),
                            tint = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                        Text(
                            location,
                            style = MaterialTheme.typography.bodySmall,
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    }
                }
                
                // Responsible + Servings + Cost
                Row(
                    horizontalArrangement = Arrangement.spacedBy(12.dp),
                    verticalAlignment = Alignment.CenterVertically
                ) {
                    if (meal.responsibleParticipantIds.isNotEmpty()) {
                        Row(
                            horizontalArrangement = Arrangement.spacedBy(4.dp),
                            verticalAlignment = Alignment.CenterVertically
                        ) {
                            Icon(
                                Icons.Default.Person,
                                contentDescription = null,
                                modifier = Modifier.size(14.dp),
                                tint = MaterialTheme.colorScheme.onSurfaceVariant
                            )
                            Text(
                                pluralStringResource(
                                    R.plurals.meal_responsible_count,
                                    meal.responsibleParticipantIds.size,
                                    meal.responsibleParticipantIds.size
                                ),
                                style = MaterialTheme.typography.bodySmall,
                                color = MaterialTheme.colorScheme.onSurfaceVariant
                            )
                        }
                    }
                    
                    Row(
                        horizontalArrangement = Arrangement.spacedBy(4.dp),
                        verticalAlignment = Alignment.CenterVertically
                    ) {
                        Icon(
                            Icons.Default.Restaurant,
                            contentDescription = null,
                            modifier = Modifier.size(14.dp),
                            tint = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                        Text(
                            pluralStringResource(R.plurals.meal_serving_count, meal.servings, meal.servings),
                            style = MaterialTheme.typography.bodySmall,
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    }
                    
                    Text(
                        stringResource(R.string.meal_currency_amount, meal.estimatedCost / 100.0),
                        style = MaterialTheme.typography.bodySmall,
                        fontWeight = FontWeight.Bold,
                        color = MaterialTheme.colorScheme.primary
                    )
                }
                
                // Status badge
                AssistChip(
                    onClick = {},
                    label = { Text(statusLabel) },
                    colors = AssistChipDefaults.assistChipColors(
                        containerColor = when (meal.status) {
                            MealStatus.COMPLETED -> MaterialTheme.colorScheme.tertiary
                            MealStatus.IN_PROGRESS -> MaterialTheme.colorScheme.primary
                            MealStatus.CANCELLED -> MaterialTheme.colorScheme.error
                            else -> MaterialTheme.colorScheme.secondary
                        },
                        labelColor = when (meal.status) {
                            MealStatus.COMPLETED -> MaterialTheme.colorScheme.onTertiary
                            MealStatus.IN_PROGRESS -> MaterialTheme.colorScheme.onPrimary
                            MealStatus.CANCELLED -> MaterialTheme.colorScheme.onError
                            else -> MaterialTheme.colorScheme.onSecondary
                        }
                    )
                )
            }
            
            // Delete button
            IconButton(
                onClick = onDeleteClick,
                modifier = Modifier.clearAndSetSemantics {
                    contentDescription = deleteDescription
                }
            ) {
                Icon(
                    Icons.Default.Delete,
                    contentDescription = null,
                    tint = MaterialTheme.colorScheme.error
                )
            }
        }
    }
}

// Helper functions

@Composable
fun getMealTypeLabel(type: MealType): String = stringResource(when (type) {
    MealType.BREAKFAST -> R.string.meal_type_breakfast
    MealType.LUNCH -> R.string.meal_type_lunch
    MealType.DINNER -> R.string.meal_type_dinner
    MealType.SNACK -> R.string.meal_type_snack
    MealType.APERITIF -> R.string.meal_type_aperitif
})

